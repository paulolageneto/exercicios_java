
package aula02;

public class operacoes {

    public static void main(String[] args) {
        int n1 = 10, n2 = 20;
        int total;
        total = n1 + n2;
        System.out.println("soma: " + total);
        total = n1 - n2;
        System.out.println("subtração: " + total);
        total = n1 * n2;
        System.out.println("multiplicação: " + total);
        total = n1 / n2;
        System.out.println("divisão: " + total);
    }

}
